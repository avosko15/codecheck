def add(num1, num2):
    print(float(num1) + float(num2))

def subtract(num1, num2):
    print(float(num1) - float(num2))

def multi(num1, num2):
    print(float(num1) * float(num2))

def divide(num1, num2):
    print(float(num1) / float(num2))
    